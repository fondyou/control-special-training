#include <stdio.h>

int main()
{
	int a = 1;
	int *ptr = &a;

	printf("%d \n",a);
	printf("%d \n",*ptr);  //打印出来的值相等

	printf("%p \n",&a);
	printf("%p \n",ptr);   //打印出来的地址相等
}

